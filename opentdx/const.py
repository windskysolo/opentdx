    HK_GEM =  (0x0030, "香港创业板")
    GGT =     (0x0047, "港股通")
    US =      (0x004a, "美股")
    # 完全兼容 EX_MARKET 的value
    HSI =     (0x2ee1, "恒指成分股")
    HSHC =    (0x2ee2, "恒生红筹")
    HSGQ =    (0x2ee4, "恒生国企")
    HSGZ =    (0x2ee7, "恒生国指")
    HSKJ =    (0x2eec, "恒生科技")
    USZGG =   (0x32c9, "美股中概股")
    USZM =    (0x32ca, "知名美股")
    
    def __init__(self, code: int, display_name: str):
        self._code = code
        self._display_name = display_name
    
    @property
    def code(self):
        """返回真实的数字code"""
        return self._code

    @property
    def display_name(self):
        """返回显示名称"""
        return self._display_name

    @classmethod
    def has_code(cls, code):
        """
        传入一个数字code，判断是否是枚举里的值
        :return: True / False
        """

        return int(code) in (item.value for item in cls)

    
